def format_values(l: list):
    return f"{l[0]} HP / {l[1]} Atk / {l[2]} Def / {l[3]} SpA / {l[4]} SpD / {l[5]} Spe"


class Species:
    def __init__(self, name, forms: dict):
        self.name = name
        self.forms = forms

    def getFormName(self, form):
        if form != "" and form not in self.forms:
            raise IndexError("Invalid form")
        return list(self.forms.keys())[0] if form == "" else form


class Mon:
    def __init__(self, species: str, **kwargs):
        self.species = natDex[species]
        self.givenForm = kwargs.get("form", "")
        self.form = self.species.getFormName(self.givenForm)
        self.dexNo = list(natDex.keys()).index(self.species.name) + 1
        self.pic = "https://www.serebii.net/art/th/{}{}.png"\
            .format(self.dexNo, "-m" if self.form == "Mega" else "-a" if self.form == "Alolan" else "-mx"
                    if self.form == "Mega X" else "-my" if self.form == "Mega Y" else
                    formPicDict.get(self.species.name, {}).get(self.form, ""))
        self.generation = 7 if self.dexNo >= 722 else 6 if self.dexNo >= 650 else 5 if self.dexNo >= 494 else\
            4 if self.dexNo >= 387 else 3 if self.dexNo >= 252 else 2 if self.dexNo >= 152 else 1
        self.data = self.species.forms[self.form]
        self.base = self.data[:6]
        self.level = kwargs.get("level", 100)
        self.evs = kwargs.get("evs", [0, 0, 0, 0, 0, 0])
        self.ivs = kwargs.get("ivs", (31, 31, 31, 31, 31, 31))
        self.hp = hcal(self.base[0], self.level, self.evs[0], self.ivs[0])
        self.nature = kwargs.get("nature", "Hardy")
        self.atk, self.dfn, self.spa, self.spd, self.spe = \
            tuple(round(scal(self.base[n], self.level, self.evs[n], self.ivs[n]) * nateff(self.nature)[n - 1])
                  for n in range(1, 6))
        self.stats = {"atk": self.atk, "def": self.dfn, "spa": self.spa, "spd": self.spd, "spe": self.spe}
        self.type1, self.type2, self.height, self.weight = self.data[6:]
        if self.form != "":
            self.speciesName = " ".join((self.form, self.species.name))
        else:
            self.speciesName = self.species.name
        self.name = kwargs.get("nick", self.speciesName)
        self.hpc = self.hp - kwargs.get("damage", 0)
        self.condit = kwargs.get("condit", None)
        self.ability = kwargs.get("ability", None)
        self.gender = kwargs.get("gender", genders.get(self.species.name, 1 if random() < 0.5 else -1))
        self.heldItem = kwargs.get("item", None)
        self.moves = kwargs.get("moves", [])
        self.moves = [dc(movedict.get(g, g)) for g in self.moves if g in movedict or type(g) == Move]
        self.moves = {g.name: g for g in self.moves}
        self.lowerMoves = {g.lower(): g for g in self.moves}
        self.team = kwargs.get("team", -1)

        # VOLATILE CONDITIONS
        self.conditTime = 0
        self.statStages = {"atk": 0, "def": 0, "spa": 0, "spd": 0, "spe": 0, "acc": 0, "eva": 0, "crt": 0}
        self.foreseen = False
        self.stockpile = 0
        self.aquaRing = False
        self.switching = None
        self.protecting = False  # whether or not Protect is active this turn
        self.protectCount = 0  # how many times this mon has used Protect
        self.resting = False  # rests after using Hyper Beam, Giga Impact
        self.twoTurn = None
        self.failed = False

    def speciesSD(self):
        alters = [g for g in self.species.forms if g != self.form]
        return {"s": f"{self.speciesName} (#{threeDig(self.dexNo)})",
                "d": f"**Type:** {self.format_types()}\n"
                f"**Height:** {str(self.height)} m | **Weight:** {str(self.weight)} kg\n"
                f"**Base stats:** {format_values(self.base)} ({sum(self.base)})",
                "footer": (f"Other forms: {', '.join(alters)}" if len(alters) > 0 and self.givenForm == "" else None),
                "thumb": self.pic}

    def volatile_str(self):
        ret = [""]
        if self.condit is not None:
            ret.append(f"- *{self.condit}*")
        if self.aquaRing:
            ret.append("+ *Aqua Ring*")
        for i in self.statStages:
            if self.statStages[i] != 0:
                ret.append(f"{stats[i]}: {signed(self.statStages[i])}")
        return "\n".join(ret)

    def name_str(self):
        return f"{self.name} (lv{self.level} {self.speciesName}{genDict[self.gender]})"

    def sd(self, full=True):
        return {"s": self.name_str(),
                "d": (f"**{self.format_types()}**-type\n**Ability:** {self.ability}\n**Holding:** {self.heldItem}\n"
                      if full else "") + f"**HP:** {self.hpc}/{self.hp}" + self.volatile_str()}

    def __str__(self):
        return f"{self.name_str()} [{self.hpc}/{self.hp} HP]" + self.volatile_str()

    def profile(self):
        return {"s": self.name_str(),
                "d": f"**Nature:** {self.nature} | **Ability:** {self.ability} | **Holding:** {self.heldItem}\n"
                f"**EVs:** {format_values(self.evs)}\n**IVs:** {format_values(self.ivs)}"}

    def pack(self):
        return {"species": self.species.name, "form": self.form, "level": self.level, "evs": self.evs, "ivs": self.ivs,
                "nature": self.nature, "nick": self.name, "condit": self.condit, "ability": self.ability,
                "gender": self.gender, "item": self.heldItem, "moves": self.moves, "damage": round(self.hp - self.hpc),
                "team": self.team}

    def de_get(self):
        return self.species.name + (f" {self.form}" if self.form != "" else "")

    def eff(self, t):
        return round(eff(t, self.type1) * eff(t, self.type2), 2)

    def print_abil(self):
        return f"-= {self.name}'s {self.ability}! =-"

    def types(self):
        return [self.type1, self.type2]

    def valid_move(self, move: str, sudo=False):
        return move.lower() in lowerMoves and\
               (sudo or (move.lower() in self.lowerMoves and self.moves[self.lowerMoves[move.lower()]].ppc > 0))

    def stat_level(self, stat):
        n = 3 if stat in ["eva", "acc"] else 2
        base = 1 if stat in ["eva", "acc"] else self.stats[stat]
        change = self.statStages[stat]
        return base * (n + (0 if change <= 0 else change)) / (n - (0 if change >= 0 else change))

    def format_types(self):
        if self.type2 is None:
            return self.type1
        return "/".join(self.types())

    def typeEffSD(self):
        four = [g for g in superEffective if self.eff(g) == 4]
        fs = f"**4x:** {', '.join(four)}\n"
        two = [g for g in superEffective if self.eff(g) == 2]
        ts = f"**2x:** {', '.join(two)}\n"
        one = [g for g in superEffective if self.eff(g) == 1 and g != typeless]
        os = f"**1x:** {', '.join(one)}\n"
        half = [g for g in superEffective if self.eff(g) == 0.5]
        hs = f"**0.5x:** {', '.join(half)}\n"
        quarter = [g for g in superEffective if self.eff(g) == 0.25]
        qs = f"**0.25x:** {', '.join(quarter)}\n"
        zero = [g for g in superEffective if self.eff(g) == 0]
        zs = f"**0x:** {', '.join(zero)}"
        ss = [fs if len(four) > 0 else "", ts if len(two) > 0 else "", os if len(one) > 0 else "",
              hs if len(half) > 0 else "", qs if len(quarter) > 0 else "", zs if len(zero) > 0 else ""]
        return f"{self.speciesName} ({self.type1}/{self.type2})", "".join(ss)


